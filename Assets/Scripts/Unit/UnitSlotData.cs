using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class UnitSlotData : MonoBehaviour
{
    public FpoImport.FPOSlot slotData;
    public int slotId;

    public void SetSlotData(FpoImport.FPOSlot slotData)
    {
        this.slotData = slotData;
        slotId = slotData.slotId;
    }
}
